// To summarize the Classic Maya collapse, we can tentatively identify five strands. I acknowledge, however,
// that Maya archaeologists still disagree vigorously among themselves in part, because the different strands
// evidently varied in importance among different parts of the Maya realm; because detailed archaeological
// studies are available for only some Maya sites; and because it remains puzzling why most of the Maya
// heartland remained nearly empty of population and failed to recover after the collapse and after re-growth of
// forests.
// With those caveats, it appears to me that one strand consisted of population growth outstripping available
// resources: a dilemma similar to the one foreseen by Thomas Malthus in 1798 and being played out today in
// Rwanda, Haiti and elsewhere. As the archaeologist David Webster succinctly puts it, “Too many farmers
// grew too many crops on too much of landscape.” Compounding that mismatch between population and
// resources was the second strand: the effects of deforestation and hillside erosion, which caused a decrease
// in the amount of useable farmland at a time when more rather than less farmland was needed, and possibly
// exacerbated by an anthropogenic drought resulting from deforestation, by soil nutrient depletion and other
// soil problems, and by the struggle to prevent bracken ferns from overrunning the fields.
// The third strand consisted of increased fighting, as more and more people fought over fewer resources.
// Maya warfare, already endemic, peaked just before the collapse. That is not surprising when one reflects
// that at least five million people, perhaps many more, were crammed into an area smaller than the US state of
// Colorado (104,000 square miles). That warfare would have decreased further the amount of land available
// for agriculture, by creating no-man’s lands between principalities where it was now unsafe to farm. Bringing matters to a head was the strand of climate change. The drought at the time of the Classic collapse was not
// the first drought that the Maya had lived through, but it was the most severe. At the time of previous
// droughts, there were still uninhabited parts of the Maya landscape, and people at a site affected by drought
// could save themselves by moving to another site. However, by the time of the Classic collapse the landscape
// was now full, there was no useful unoccupied land in the vicinity on which to begin anew, and the whole
// population could not be accommodated in the few areas that continued to have reliable water supplies.
// As our fifth strand, we have to wonder why the kings and nobles failed to recognize and solve these

// seemingly obvious problems undermining their society. Their attention was evidently focused on their short-
// term concerns of enriching themselves, waging wars, erecting monuments, competing with each other, and

// extracting enough food from the peasants to support all those activities. Like most leaders throughout
// human history, the Maya kings and nobles did not heed long-term problems, insofar as they perceived them.
// Finally, while we still have some other past societies to consider before we switch our attention to the
// modern world, we must already be struck by some parallels between the Maya and the past societies. As on
// Mangareva, the Maya environmental and population problems led to increasing warfare and civil strife.
// Similarly, on Easter Island and at Chaco Canyon, the Maya peak population numbers were followed swiftly by
// political and social collapse. Paralleling the eventual extension of agriculture from Easter Island’s coastal
// lowlands to its uplands, and from the Mimbres floodplain to the hills, Copan’s inhabitants also expanded
// from the floodplain to the more fragile hill slopes, leaving them with a larger population to feed when the
// agricultural boom in the hills went bust. Like Easter Island chiefs erecting ever larger statues, eventually
// crowned by pukao, and like Anasazi elite treating themselves to necklaces of 2,000 turquoise beads, Maya
// kings sought to outdo each other with more and more impressive temples, covered with thicker and thicker
// plaster — reminiscent in turn of the extravagant conspicuous consumption by modern American CEOs. The
// passivity of Easter chiefs and Maya kings in the face of the real big threats to their societies completes our
// list of disquieting parallels.
